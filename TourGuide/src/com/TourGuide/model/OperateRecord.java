package com.TourGuide.model;

public class OperateRecord {
	
	/**
	 * 操作人员账号，操作人员角色，操作人员所属景区，操作人员时间信息，操作日期
	 */
	private String operateAccount;
	private String operateRole;
	private String operateScenic;
	private String operateMessage;
	private String operateDate;
	public String getOperateAccount() {
		return operateAccount;
	}
	public void setOperateAccount(String operateAccount) {
		this.operateAccount = operateAccount;
	}
	public String getOperateRole() {
		return operateRole;
	}
	public void setOperateRole(String operateRole) {
		this.operateRole = operateRole;
	}
	public String getOperateScenic() {
		return operateScenic;
	}
	public void setOperateScenic(String operateScenic) {
		this.operateScenic = operateScenic;
	}
	public String getOperateMessage() {
		return operateMessage;
	}
	public void setOperateMessage(String operateMessage) {
		this.operateMessage = operateMessage;
	}
	public String getOperateDate() {
		return operateDate;
	}
	public void setOperateDate(String operateDate) {
		this.operateDate = operateDate;
	}
	
	

}
