package com.TourGuide.model;



public class RecordofReward {
	
	private String guidePhone;
	private String guideName;
	private String  cla;
	private int money;
	private String date;
	private String reason;
	private String operatePhone;
	private String operateRole;
	public String getGuidePhone() {
		return guidePhone;
	}
	public void setGuidePhone(String guidePhone) {
		this.guidePhone = guidePhone;
	}
	public String getGuideName() {
		return guideName;
	}
	public void setGuideName(String guideName) {
		this.guideName = guideName;
	}
	public String getCla() {
		return cla;
	}
	public void setCla(String cla) {
		this.cla = cla;
	}
	public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	public String getOperatePhone() {
		return operatePhone;
	}
	public void setOperatePhone(String operatePhone) {
		this.operatePhone = operatePhone;
	}
	public String getOperateRole() {
		return operateRole;
	}
	public void setOperateRole(String operateRole) {
		this.operateRole = operateRole;
	}
	
	
	
}
